class Calculator {
	public void add(int x, int y) {
		println(x + y);
	}
	
	public void subtract(int x, int y) {
		println(x - y);
	}
	
	public void multiply(int x, int y) {
		println(x * y);
	}
	
	public void	divide(int x, int y) {
		println((double) x / (double) y);
	}
	
	public void modulus(int x, int y) {
		println(x % y);
	}
}